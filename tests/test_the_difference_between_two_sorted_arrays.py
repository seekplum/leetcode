# -*- coding: utf-8 -*-

import pytest

from src.the_difference_between_two_sorted_arrays import divide


@pytest.mark.parametrize("nums1, nums2, result", [
    ([1, 3], [2, 3], [1]),
    ([1, 3, 4, 5, 6, 7], [2, 3], [1, 4, 5, 6, 7]),
    ([1, 3, 4, 5, 6, 7], [2, 3, 4, 6, 7], [1, 5]),
    ([1], [2, 3, 4, 6, 7], [1]),
    ([1, 2, 3], [2, 3, 4, 6, 7], [1]),
    ([1, 2, 4], [2, 3, 4, 6, 7], [1]),
    ([1, 2, 6], [2, 3, 4, 6, 7], [1]),
    ([1, 2, 7], [2, 3, 4, 6, 7], [1]),
    ([2, 3, 4, 6, 7], [2, 3, 4, 6, 7], []),
    ([1, 2, 3, 4, 6, 7], [], [1, 2, 3, 4, 6, 7]),
    ([1, 3, 5, 7, 9], [2, 4, 6, 8, 10], [1, 3, 5, 7, 9]),
    ([1, 3, 5, 7, 9], [2, 4, 6, 8, 10, 9], [1, 3, 5, 7, 9]),
])
def test_divide(nums1, nums2, result):
    assert divide(nums1, nums2) == result
